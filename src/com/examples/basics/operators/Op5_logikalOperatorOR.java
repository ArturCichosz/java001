package com.examples.basics.operators;

public class Op5_logikalOperatorOR {
    public static void main(String[] args) {

        // Żeby wynik był false, to wynik po obu stronach musi być false

        System.out.println(5 >4 || 5 < 4); // true
        System.out.println(4 > 5 || 5 == 4); // false

    }
}
