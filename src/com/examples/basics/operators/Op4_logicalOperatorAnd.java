package com.examples.basics.operators;

public class Op4_logicalOperatorAnd {
    public static void main(String[] args) {

        // koniunkcja operator logiczny - AND

        System.out.println(5 < 10  && 20 >= 15); // true
        System.out.println(5 > 10 && 20 > 15);  // false
        System.out.println(4 > 2 && 10 == 11); // false
        System.out.println(4 == 2 && 12 > 20); // false

        //
    }
}
