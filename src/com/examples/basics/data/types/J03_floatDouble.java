package com.examples.basics.data.types;

public class J03_floatDouble {
    public static void main(String[] args) {
        float number = 10.5f; // zawsze dodawać F, żeby program wiedział ze to float
        double bigNumber = 23453543543d;

        System.out.println("Number: " + number + " bigNumber: " + bigNumber);
    }
}
